#!/usr/bin/env node
// @hook-event: PreToolUse
// @hook-owner: deepflow
/**
 * deepflow codebase-map injection hook
 * PreToolUse hook: fires before a Task tool executes.
 *
 * Maps agent-name (subagent_type) → artifact subset, reads those artifacts from
 * `.deepflow/maps/{spec}/` (or `.deepflow/codebase/` as fallback), concatenates
 * their content, and injects into the agent prompt via updatedInput.
 *
 * Agent → artifact subset table (canonical, single source of truth):
 *   df-implement   → CONVENTIONS.md, TESTING.md
 *   df-test        → TESTING.md, CONVENTIONS.md
 *   df-integration → CONVENTIONS.md, TESTING.md, ARCHITECTURE.md
 *   df-spike       → STACK.md, ARCHITECTURE.md, INTEGRATIONS.md
 *   df-optimize    → ARCHITECTURE.md, CONVENTIONS.md
 *   df-haiku-ops   → (none)
 *
 * Lazy: if `.deepflow/maps/{spec}/` does not exist, falls back to
 * `.deepflow/codebase/`. If neither directory exists, exits silently (AC-4).
 *
 * Pass-through for non-Task tools (tool_name !== 'Task') and for agents not
 * in the table (returns no output — Claude Code treats missing output as allow).
 *
 * Failure policy: ANY error exits silently with code 0 and empty stdout.
 * Never blocks tool execution.
 */

'use strict';

const fs = require('fs');
const path = require('path');
const { readStdinIfMain } = require('./lib/hook-stdin');

// ── Constants ────────────────────────────────────────────────────────────────

/** Primary artifact directory (per-spec maps). */
const MAPS_DIR = path.join('.deepflow', 'maps');

/** Global fallback artifact directory (generated by /df:map). */
const CODEBASE_DIR = path.join('.deepflow', 'codebase');

/** Deduplication marker — prevents double-injection on re-entrant Task spawns. */
const INJECTION_MARKER = '<!-- df-codebase-inject -->';

/**
 * Canonical agent → artifact-subset mapping.
 *
 * Keys match the `subagent_type` value passed to Task(subagent_type="…").
 * Values are ordered arrays of artifact filenames to inject.
 * An empty array means "inject nothing" (pass-through for that agent type).
 *
 * This table is the single source of truth — keep in sync with:
 *   - specs/doing-codebase-map.md REQ-4
 *   - bin/wave-runner.js subagent routing table
 */
const AGENT_ARTIFACT_MAP = {
  'df-implement':   ['CONVENTIONS.md', 'TESTING.md'],
  'df-test':        ['TESTING.md', 'CONVENTIONS.md'],
  'df-integration': ['CONVENTIONS.md', 'TESTING.md', 'ARCHITECTURE.md'],
  'df-spike':       ['STACK.md', 'ARCHITECTURE.md', 'INTEGRATIONS.md'],
  'df-optimize':    ['ARCHITECTURE.md', 'CONVENTIONS.md'],
  'df-haiku-ops':   [],
};

// ── Spec extraction ──────────────────────────────────────────────────────────

/**
 * Extract the spec name from the agent prompt.
 *
 * Looks for patterns like:
 *   - "Spec: <name>"  (execute.md task prompt format)
 *   - "WORKDIR: ...worktrees/<name>" (worktree boundary preamble)
 *
 * Returns the spec slug string or null if not found.
 *
 * @param {string} prompt
 * @returns {string|null}
 */
function extractSpecName(prompt) {
  if (!prompt || typeof prompt !== 'string') return null;

  // Prefer explicit "Spec: <name>" declaration (most reliable)
  const specMatch = prompt.match(/\bSpec:\s+([A-Za-z0-9_-]+)/);
  if (specMatch) return specMatch[1];

  // Fall back to worktree path embedded in WORKDIR preamble
  const worktreeMatch = prompt.match(/\.deepflow\/worktrees\/([A-Za-z0-9_-]+)/);
  if (worktreeMatch) return worktreeMatch[1];

  return null;
}

// ── Artifact resolution ──────────────────────────────────────────────────────

/**
 * Resolve the directory from which to load artifacts for the given spec.
 *
 * Resolution order (first existing directory wins):
 *   1. `.deepflow/maps/{spec}/`   — per-spec map produced by /df:map
 *   2. `.deepflow/codebase/`      — global fallback
 *
 * Returns the absolute path of the chosen directory, or null if neither exists.
 *
 * @param {string} cwd   - Project root.
 * @param {string|null} specName - Spec slug (may be null).
 * @returns {string|null}
 */
function resolveArtifactDir(cwd, specName) {
  // Try per-spec directory first
  if (specName) {
    const specDir = path.join(cwd, MAPS_DIR, specName);
    if (fs.existsSync(specDir)) return specDir;
  }

  // Fall back to global codebase directory
  const globalDir = path.join(cwd, CODEBASE_DIR);
  if (fs.existsSync(globalDir)) return globalDir;

  // Neither exists — lazy skip
  return null;
}

// ── Artifact loading ─────────────────────────────────────────────────────────

/**
 * Read and concatenate the content of all requested artifact files from
 * `artifactDir`. Missing files are silently skipped.
 *
 * @param {string}   artifactDir - Absolute path to the artifact directory.
 * @param {string[]} artifacts   - Ordered list of artifact filenames to load.
 * @returns {string} Concatenated content, or empty string if all are missing.
 */
function loadArtifacts(artifactDir, artifacts) {
  const parts = [];

  for (const filename of artifacts) {
    const filePath = path.join(artifactDir, filename);
    try {
      const content = fs.readFileSync(filePath, 'utf8').trim();
      if (content) {
        parts.push(`\n\n---\n## Codebase Context: ${filename}\n\n${content}`);
      }
    } catch (_) {
      // Missing artifact — skip silently (lazy contract)
    }
  }

  return parts.join('');
}

// ── Main logic ───────────────────────────────────────────────────────────────

/**
 * Core hook logic. Returns the hookSpecificOutput JSON object to emit, or null
 * if no injection is needed (pass-through).
 *
 * @param {object} payload - Parsed stdin JSON from Claude Code.
 * @returns {object|null}
 */
function main(payload) {
  const { tool_name, tool_input, cwd } = payload || {};

  // Pass-through for non-Task tools
  if (tool_name !== 'Task') return null;
  if (!tool_input || typeof tool_input !== 'object') return null;

  const subagentType = (tool_input.subagent_type || '').toLowerCase().trim();

  // Only handle known agents — unknown agents pass through unmodified
  if (!Object.prototype.hasOwnProperty.call(AGENT_ARTIFACT_MAP, subagentType)) {
    return null;
  }

  const artifacts = AGENT_ARTIFACT_MAP[subagentType];

  // Agent is known but requests no artifacts — allow without modification
  if (!artifacts || artifacts.length === 0) {
    return null;
  }

  const prompt = tool_input.prompt || '';

  // Deduplication guard — never inject twice
  if (prompt.includes(INJECTION_MARKER)) {
    return null;
  }

  const effectiveCwd = cwd || process.cwd();

  // Resolve which spec this task belongs to (best-effort)
  const specName = extractSpecName(prompt);

  // Resolve the artifact directory — lazy skip if neither dir exists
  const artifactDir = resolveArtifactDir(effectiveCwd, specName);
  if (!artifactDir) {
    return null; // AC-4: lazy — skip silently
  }

  // Load artifact content
  const injectedContent = loadArtifacts(artifactDir, artifacts);
  if (!injectedContent) {
    return null; // All artifacts missing — skip silently
  }

  const updatedPrompt = `${prompt}\n\n${INJECTION_MARKER}${injectedContent}`;

  return {
    hookSpecificOutput: {
      hookEventName: 'PreToolUse',
      permissionDecision: 'allow',
      updatedInput: {
        ...tool_input,
        prompt: updatedPrompt,
      },
    },
  };
}

// ── Entry point ──────────────────────────────────────────────────────────────

readStdinIfMain(module, (payload) => {
  try {
    const result = main(payload);
    if (result) {
      process.stdout.write(JSON.stringify(result));
    }
    // No output → Claude Code treats as allow (pass-through)
  } catch (_) {
    // Fail-open: any error → silent exit 0, no stdout
  }
});

module.exports = {
  main,
  extractSpecName,
  resolveArtifactDir,
  loadArtifacts,
  AGENT_ARTIFACT_MAP,
  INJECTION_MARKER,
  MAPS_DIR,
  CODEBASE_DIR,
};
